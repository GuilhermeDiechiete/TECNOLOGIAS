#ANALISE DE TEXTOS


nome = 'Guilherme Diechiete'

print(len(nome)) #len -> mostra a quantidade de caracteres

print(nome.count('e')) #vai mostrar quantos e tem
print(nome.count('e',0 , 13)) #vai mostrar quantos 'e' tem do 0 ao 13

print(nome.find('lhe')) #mostra quantas vezes uma cadeia de caracteres aparece, (mostra onde comecou)
print(nome.find('android')) # vai mostrar menos 1 quando nao tem

