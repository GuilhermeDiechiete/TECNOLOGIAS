# TRANSFORMAÇÕES

nome = 'Guilherme Diechiete'

# PARA EU ATRIBUIR As TRANSFORMAÇÕES, E SO COLOCAR
#VARIAVEL COM A TRANSFORMAÇÃO

# REPLACE, troca um nome pelo outro
print('O {} é muito inteligente'.format(nome.replace('Guilherme','Luana')))

#UPPER -> joga tudo para maiusculo
print(' {} '.format(nome.upper()))

#LOWER -> joga tudo para minusculo
print(' {} '.format(nome.lower()))

# CAPITALIZE -> so o primeiro caracter vai ficar em maiusculo
print(' {} '.format(nome.capitalize()))

# TITLE -> vai por todas as iniciais das palavras em maiusculo
print( ' {} '.format(nome.title()))

# STRIP -> remove espaços inuteis
print(' {} '.format(nome.strip()))

# RTRIP -> remove os ultimos espações
print(' {} '. format(nome.rstrip()))

# LSTRIP -> remove os espaços do inicio
print(' {} '. format(nome.lstrip()))






