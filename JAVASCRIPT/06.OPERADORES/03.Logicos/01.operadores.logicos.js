/* OPERADORES LOGICOS

&&  -> AND  -> E  -> exigente
||  -> OR   -> OU -> não é exigente, uma basta
!   -> NOT  -> NÃO -> ela inverte uma expressao

*/

console.log('AND -> E')
console.log(true && true) // retorna true
console.log(true && false) // retorna false
console.log(false && true) // retorna false
console.log(false && false) // retorna false

console.log('')

console.log('OR -> ou')
console.log(true || true) // retorna true
console.log(true || false) // retorna true
console.log(false || true) // retorna true
console.log(false || false) // retorna false

console.log('')

console.log('NOT -> NÃO')
console.log(true)
console.log(!true) // inverte a expressao
