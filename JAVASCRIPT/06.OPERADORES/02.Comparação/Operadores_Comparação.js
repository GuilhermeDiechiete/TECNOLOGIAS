/*

>  -> maior
>=  -> maior ou igual
<   -> menor
<=  -> menor ou igual
==  -> igual
=== -> igual no valor e tipo
!=  -> diferente
!== -> diferente no valor e tipo

*/
//OS CALCULOS SEMPRE IRAM RETORNAR ( TRUE | FALSE) boolean

console.log(5 > 2)
console.log(5 >= 2)
console.log(5 < 2)
console.log(5 <= 2)
console.log(5 == 2)
console.log(5 === 2)
console.log(5 != 2)
console.log(5 !== 2)

const n1 = 10
const n2 = 20
const comp = n1 < n2
console.log('Resposta: ' + comp)
